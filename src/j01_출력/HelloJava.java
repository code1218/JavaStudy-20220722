package j01_출력;

public class HelloJava {

	public static void main(String[] args) {
		
		// 역슬러시(\) 이스케이프문자
		
		System.out.print("Hello, Java\n");
		System.out.print("김 + 준 + 일");
		System.out.print(10 + 20 + "글자 합치기\n");
		System.out.print("a\nb\n"); // \n: 줄바꿈
		System.out.println("\"김준일\"");
		System.out.println("\'김준일\'");
		System.out.println("\t이름\t연락처\\");
		
	}

}




